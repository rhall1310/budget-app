<script>
	let shown = false;
	export let modalContent;
	export function show(c) {
		shown = true;
		modalContent = c;
	}
	export function hide() {
		shown = false;
	}
</script>

<svelte:window
	on:keydown={(e) => {
		if (e.key == 'Escape') {
			hide();
		}
	}}
/>

{#if shown}
	<div class="modal-wrapper">
		<div class="modal">
			<span class="close" on:click={() => hide()}>&times;</span>
			<slot />
		</div>
	</div>
{/if}

